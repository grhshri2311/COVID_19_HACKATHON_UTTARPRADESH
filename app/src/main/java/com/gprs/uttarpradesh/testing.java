    private void writeToFile(String data) {
        String extStorageDirectory = Environment.getExternalStorageDirectory()
                .toString();
        File f = new File(extStorageDirectory + "/COVI19RELIEF/list/" + select + ".txt");

        if (!f.exists()) {
            try {

                File folder1 = new File(extStorageDirectory, "COVI19RELIEF");// Name of the folder you want to keep your file in the local storage.
                folder1.mkdir(); //creating the folder
                File folder2 = new File(folder1, "list");// Name of the folder you want to keep your file in the local storage.
                folder2.mkdir(); //creating the folder
                File file = new File(folder2, select + ".txt");
                file.createNewFile(); // creating the file inside the folder
            } catch (Exception e) {

            }
        }
        try {
            FileOutputStream file = new FileOutputStream(f);
            DataOutputStream fileOutputStream = new DataOutputStream(file);
            fileOutputStream.writeBytes(data);
        } catch (IOException e) {
            Log.e("Exception", "File write failed: " + e.toString());
        }
    }
